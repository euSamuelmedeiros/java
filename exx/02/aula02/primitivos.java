package aula02;
// Tipos primitivos java

// int, double, float, char, byte, short, long, boolean

public class primitivos {
    public static void main(String[] args) {
        int idade = 10;
        long numerogrande = 100000;
        double salario = 3000;    
        byte idadeByte = 10;
        short idadeShort = 10;
        boolean verdadeiro = true;
        boolean falso = false;
        char caracteres = 'M';
        float pagamentoFloat = 3500;

        System.out.println("a idade é " + idade + " anos ");
        System.out.println("Número grande: " + numerogrande);
        System.out.println("Salário: " + salario);
        System.out.println("Idade Byte: " + idadeByte);
        System.out.println("Idade Short: " + idadeShort);
        System.out.println("Valor boolean verdadeiro: " + verdadeiro);
        System.out.println("Valor boolean falso: " + falso);
        System.out.println("Caractere: " + caracteres);
        System.out.println("Pagamento (float): " + pagamentoFloat);
                

    }
}



    
